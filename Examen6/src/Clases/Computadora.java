
package Clases;
import java.util.*;


public class Computadora {
    private String nombre;
    private int precio; 

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public String getNombre() {
        return this.nombre;
    }

    public int getPrecio() {
        return this.precio;
    }
    
}
